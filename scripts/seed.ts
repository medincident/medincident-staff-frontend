// Бизнес-смещение для occurredAt: 60% — рабочие часы 8–18, понедельники нагруженнее.
function randomBusinessDate(): string {
  const now = Date.now();
  const sixMo = now - 180 * 24 * 3600 * 1000;
  const t = sixMo + Math.random() * (now - sixMo);
  const d = new Date(t);
  if (d.getDay() === 0 || d.getDay() === 6) {
    // Смещаем 80% выходных событий на следующий понедельник.
    if (Math.random() < 0.8) d.setDate(d.getDate() + (d.getDay() === 0 ? 1 : 2));
  }
  // Бизнес-часы 8–18.
  if (Math.random() < 0.7) d.setHours(8 + Math.floor(Math.random() * 10), Math.floor(Math.random() * 60), 0, 0);
  return d.toISOString();
}

